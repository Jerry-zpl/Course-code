LJMP MAIN
ORG 000BH
LJMP ITOP
ORG 0030H

MAIN:MOV TMOD, #06H
MOV TL0, #0FFH
MOV TH0, #0FFH
SETB ET0
SETB EA
SETB TR0
HERE: SJMP HERE

ITOP: 
MOV DPTR, #TABLE
PUSH ACC
MOVC A, @A+DPTR
MOV P2, A
POP ACC
INC A
CJNE A, #0AH, FINAL
CLR A
FINAL: RETI

TABLE: 
DB 0F9H,0A4H,0B0H,99H,92H
DB 82H,0F8H,80H,90H,0C0H

